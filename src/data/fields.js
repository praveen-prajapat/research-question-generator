export function getField(id) {
  return FIELDS.find((f) => f.id === id) || FIELDS[FIELDS.length - 1];
}

export function getPartnerField(field) {
  const others = FIELDS.filter(
    (f) => f.id !== field.id && field.partners.includes(f.id)
  );
  if (others.length === 0) {
    return FIELDS.filter((f) => f.id !== field.id)[
      Math.floor(Math.random() * (FIELDS.length - 1))
    ];
  }
  return others[Math.floor(Math.random() * others.length)];
}
